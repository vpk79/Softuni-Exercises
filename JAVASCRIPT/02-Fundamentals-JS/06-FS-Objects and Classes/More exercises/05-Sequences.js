function sequences(JSONarr) {
    let arrBuff = [];

    for (let i = 0; i < JSONarr.length; i++) {
        let arr1 = JSON.parse(JSONarr[i]).sort((a, b) => b - a);
        let isCountainArr1 = arrBuff.some(subArray => arr1.every(element => subArray.includes(element)))

        if(!isCountainArr1){
            arrBuff.push(arr1)
        }
    }
    arrBuff.sort((a, b) => a.length - b.length);

    for (let arr of arrBuff) {
        console.log(`[${arr.join(", ")}]`);
    }
}




